# O controle de fluxo permite a tomada de decição em um programa
# operadores aritméticos
soma = 1 + 2
print(soma)

subtracao = 1 - 2
print(subtracao)

# multiplicação
multiplicacao = 1 * 2
print(multiplicacao)

# divisao 
divisao = 1 / 2
print(divisao)

# divisao inteira:
divisao = 1 // 2
print(divisao)

# resto ou módulo
modulo = 1 % 2
print(modulo)

# potenciacao
potencia = 2 ** 2
print(potencia)


# operadores lógicos
igualdade = 1 == 2  # = é atribuicao == é igualdade(comparacao)
print(igualdade)


diferenca = 1 != 2
print(diferenca)

maior = 2 > 4
print(maior)

menor = 2 < 4
print(menor)

menorigual = 2 <= 2

print(menorigual)

maiorigual = 2>=2
print(maiorigual)


# and (inclusao) or (exclusao) not (negacao)
x = 1 < 2 and 2 > 0
print(x)

print(1 < 2 or 2 > 0)

print(1 < 2 and not 2 > 0)


